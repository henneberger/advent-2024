  private static long calc(int px, int py, int vx, int vy, int ticks) {
    //I like things being one number
    int x = (px + vx * ticks) % width;
    if (x < 0) {
      x += width;
    }

    int y = (py + vy * ticks) % height;
    if (y < 0) {
      y += height;
    }

    return (long) y * width + x;
  }

